            <h1> <?php echo $title ?> </h1>
            <p>Carbon Solar ou CARBON est une société industrielle française créée en 2022 proposant de produire des cellules photovoltaïques ainsi que leurs composants (lingots et wafers) et leur assemblages. Elle ambitionne de construire d’ici 2025 une usine de production représentant un investissement de 1,5 milliards d’euros.</p>
            <p>Les wafers et cellules photovoltaïques sont aujourd’hui quasi-exclusivement produites en Chine, ce qui expose la production photovoltaïque mondiale aux risques géopolitiques. CARBON propose d’en produire en France, ainsi que leurs assemblage, en se positionnant sur des technologies d’avenir.</p>
            <h2>Les technologies Carbon Solar (CARBON)</h2>
            <p>CARBON va proposer trois types de produits: produit des cellules photovoltaïques ainsi que leurs composants (lingots et wafers) et assemble ensuite ces cellules photovoltaïques pour en faire des modules.</p>
            <p>Si les wafers de type P dominent le marché, le type N permettrait « une évolution plus rapide et dynamique des technologies cellules ». C’est le type de wafers qu’ils ont choisi.</p>
            <p>La technologie de cellules solaires à haut rendement qu’ils ont choisi est dite TOPCon (Tunnel Oxide Passivated Contact). Elle permet un « meilleur coefficient de température, pas ou peu de dégradation des performances dues au LID et au LeTID ainsi qu’un meilleur coefficient de bifacialité » que sa concurrente, dominante aujourd’hui, PERC.</p>
            <p>Ils développent également des cellules utilisant la technologie IBC (Interdigitated Back Contact). Leurs atouts sont « un rendement surfacique plus élevé (+Wc/m²), une meilleure performance à basse irradiance, une plus grande résistance aux points chauds et une esthétique améliorée. » (Carbon)</p>
            <h2>Financement et histoire de Carbon Solar</h2>
            <p>Carbon Solar ou CARBON (ce dernier nom est à privilégier) a été créé en mars 2022 à Roche-la-Molière (Loire)</p>
            <p>Ils ont annoncé en mars 2023 un projet de gigafactory à Fos-sur-mer qui pourra produire 5GW de cellules photovoltaïques et 3,5GW de modules. La construction s’achèverait fin 2025 et le projet représenterait 1,5 milliards d’euros d’investissement et créerait directement plus de 3000 emplois. Il est prévu d’y ajouter 4 milliards d’euros pour, d’ici 2030, pouvoir produire 20GW.</p>
