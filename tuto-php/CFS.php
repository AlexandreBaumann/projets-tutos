            <h1> <?php echo $title ?> </h1>
            <p>CommonWealth Fusion Systems (CFS) est l'entreprise développant la fusion nucléaire la plus financée: 1.8 milliards d'euros. La technologie étudiée est celle du tokamak.</p>
            <h2>La fusion nucléaire CommonWealth Fusion Systems</h2>
            <p>CommonWealth Fusion System développe la fusion nucléaire en tokamak: les noyaux de deutérium et de tritium sont accélérés dans une parcours circulaire (torroidal pour être plus précis: ils vrillent) jusqu'à ce que, en se percutant, ils fusionnent. Le principal enjeu est, pour l'instant, de réussir à produire plus d'énergie qu'on en consomme.</p>
            <p>Toutefois, avant cela, il faut réussir à confiner le plasma. C'est pour cela que l'entreprise s'est concentrée sur le développement de nouveaux aimants supraconducteurs à haute température (high temperature superconductor, HTS) cette étape s'est finie en septembre 2021 avec la démonstration à l'échelle de la performance d'un aimant de 20 tesla. Le principal challenge était à la fois de pouvoir accélérer les particules et, en même temps, de confiner le plasma grâce à un champ magnétique, pour qu'il ne détruise pas l'installation.</p>
            <p>L'étape suivante consiste à construire un démonstrateur, le SPARC, visant à réussir à produire plus d'énergie qu'il n'en consomme (c'est le point d'ignition). CFS allègue que sa performance serait similaire à ITER, mais avec un tokamak 10 fois plus petit.</p>
            <h2>Histoire et financement de CommonWealth Fusion Systems</h2>
            <p>CFS est un spin-off du Plasma Science and Fusion Center du MIT fondé en 2018 à Cambridge (Massachusetts, USA) avec un investissement initial de 50 millions de dollars. Il reprend les recherches sur le tokamak « Alcator-C-Mod« , conduites au MIT.</p>
            <p>Très rapidement, les fonds affluent:</p>
            <ul>
                <li>Son tour de financement de série A ramène 115M$ en juin 2019, avec notamment ENI, Breakthrough Energy Ventures et Khosla Ventures.</li>
                <li>Un tour de série A2 ramène 84 M$ supplémentaires en mai 2020 auprès, notamment, de Temasek, Equinor et Devonshire Investors.</li>
                <li>En 2021, suite à l'annonce du succès d'une expérimentation sur son aimant supraconducteur, CFS lève 1.8 milliards de dollars, notamment pour financer la construction de 2 tokamaks: un expérimental, SPARC, et un commercial, ARC. Ce tour était mené par Tiger Global Management et inclut de nouveaux investisseurs (notamment Bill Gates; Coatue; DFJ Growth; Emerson Collective; Footprint Coalition; Google; JIMCO Technology Fund, part of JIMCO, the Jameel Family's global investment arm; John Doerr; JS Capital; Marc Benioff's TIME Ventures) et des investisseurs historiques (notamment Breakthrough Energy Ventures; The Engine; Eni; Equinor; Fine Structure Ventures; Future Ventures; Hostplus; Khosla Ventures; Lowercarbon; Moore Strategic Ventures; Safar Partners; Schooner Capital; Soros Fund Management LLC; Starlight Ventures; Temasek).</li>
            </ul>
            <p>Avec ces fonds, CommonWealth Fusion Systems continue à avancer dans le développement de sa technologie de fusion nucléaire. La réussite de la construction et du fonctionnement des tokamaks SPARC et ARC pourrait représenter une étape majeure dans la réalisation de la fusion nucléaire comme source d'énergie propre et durable pour l'avenir.</p>
